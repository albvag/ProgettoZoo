    private void jselezionaspecieActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jselezionaspecieActionPerformed
             DBConnect conn=new DBConnect();
       String specie=this.jselezionaspecie.getSelectedItem().toString();
       ArrayList<Animale> lista= new ArrayList<Animale>();
       
       lista=conn.selezionaAnimaliNome(specie);
       if(!lista.isEmpty())
       {  this.jselanim.removeAllItems();
     for(int j = 0; j < lista.size(); j++)
        {
            
            this.jselanim.addItem(lista.get(j).getNome());
            
            
        }}
    }//GEN-LAST:event_jselezionaspecieActionPerformed

    private void jConfermaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jConfermaActionPerformed
        String specie = this.jselezionaspecie.getSelectedItem().toString();
        String nome = this.jselanim.getSelectedItem().toString();
        DBConnect conn = new DBConnect();
        ProgettoZoo pz =new ProgettoZoo();
        String nostro;
        String salute;
        //ArrayList<Animale> animlist =new ArrayList<Animale>();
        Animale an=new Animale();
        Habitat hab=new Habitat();
        ArrayList<Habitat> list =new ArrayList<Habitat>();
          list = conn.selezionaHabitat();
          ArrayList<Animale> lista = conn.selezionaSpecie();
          
         
        an=conn.selezionaAnimale(nome, specie);
        
            this.jAggiunginomeanimale.setText(an.getNome());
       
            
          
       
              for(int i = 0; i < lista.size(); i++)
        {
            
            this.jselspecie.addItem(lista.get(i).getSpecie());
            
        }
              this.jselspecie.setSelectedItem(an.getSpecie()); 
            
               
               this.jselhabitat.setText(conn.selezionaHabitatSpecie(this.jselspecie.getSelectedItem().toString()).getHabitat());
            
            
            
            this.jGenere.setSelectedItem(an.getSesso());
            String DataNascita = an.getDataNascita().toString();
           String DataNascita_String = pz.NuovoFormatoData(DataNascita, "yyyy-MM-dd", "dd-MM-yyyy");
            this.jDatadinascita.setText(DataNascita_String);
            
            if(an.getNostro()== true)
            {    nostro="SI";
            this.jnostro.setSelectedItem(nostro);
            }else{nostro="NO";
             this.jnostro.setSelectedItem(nostro);
            }
           
             if(an.getSalute()== true)
             {
                 salute="SANO";
                 this.jsalute.setSelectedItem(salute);
             }else{
                 salute="MALATO";
                 this.jsalute.setSelectedItem(salute);
             }
            this.jInternalFrame1.setVisible(true);
            pack();
        
    }//GEN-LAST:event_jConfermaActionPerformed

